  m <- function(x) matrix(x, ncol = 2, byrow = TRUE)

  m_nosquash <- m(c(1, 5))
  m_squashed <- m(c(1, 5, 4, 7))
  m_na       <- m(c(NA_real_, NA_real_))
  m_empty    <- m(numeric())

  expect_equal(
    squash()
  )
})

